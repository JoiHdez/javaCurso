    private void SA1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SA1MouseClicked
      String usu=Usu.getText();
      String pass=Contr.getText();
        if(usu.equals("Joel")&& pass.equals("123")|| usu.equals("Omar")&& pass.equals("isaias")){
         JOptionPane.showMessageDialog(rootPane,"Contrase√±a Correcta");
        new Menu().setVisible(true);
           this.dispose();
         }else {
            JOptionPane.showMessageDialog(this,"Contrase√±a Incorrecta");
            Usu.setText("");
            Contr.setText("");
            Usu.requestFocus();
            cont++;
        }
        if(cont>=3){
            JOptionPane.showMessageDialog(this,"Se excedio intente mas tarde");
            new Salir().setVisible(true);
           this.dispose();
        }
    }//GEN-LAST:event_SA1MouseClicked

